<label for="post-1" class="first-trigger">1. What is fleet cover insurance?</label> 
<div class="first-wrap"> <span class="first-target"> 
  <p>Fleet cover insurance is a type of insurance that provides coverage for a group of vehicles owned by a business. It can protect against various risks, including accidental damage, theft, third-party liability, and more.</p> 
  </span> </div> 
<label for="post-2" class="second-trigger">2. How many vehicles do I need to have to qualify for fleet cover insurance?</label> 
<div class="second-wrap"> <span class="second-target"> 
  <p>The minimum number of vehicles required to qualify for fleet cover insurance varies by insurance provider, but it is typically around five vehicles.</p> 
  </span> </div> 
<label for="post-3" class="third-trigger">3. Can I add or remove vehicles from my fleet cover insurance policy?</label> 
<div class="third-wrap"> <span class="third-target"> 
  <p>Yes, most insurance providers allow you to add or remove vehicles from your fleet cover insurance policy. This can be done by contacting your insurance provider directly.</p> 
  </span> </div> 
<label for="post-4" class="fourth-trigger">4. Do I need to insure all my vehicles under the same policy?</label> 
<div class="fourth-wrap"> 
<span class="fourth-target"> 
<p>No, you do not necessarily need to insure all your vehicles under the same policy. Some insurance providers may allow you to have multiple policies for different types of vehicles or for vehicles with different levels of risk.</p> 
</span> 
</div> 
<label for="post-5" class="fifth-trigger">5. What is covered under fleet cover insurance?</label> 
<div class="fifth-wrap"> <span class="fifth-target"> 
  <p>Fleet cover insurance typically covers accidental damage, theft, third-party liability, and other risks that may be specific to your business.</p> 
  </span> </div> 
<label for="post-6" class="sixth-trigger">6. How can I reduce the cost of my fleet cover insurance?</label> 
<div class="sixth-wrap"> <span class="sixth-target"> 
  <p>There are several ways to reduce the cost of your fleet cover insurance, such as increasing your excess, installing telematics devices to monitor driving behavior, and providing driver safety training.</p> 
  </span> </div>
<label for="post-7" class="seventh-trigger">7. What happens if one of my drivers is involved in an accident?</label> 
<div class="seventh-wrap"> <span class="seventh-target"> 
  <p>If one of your drivers is involved in an accident, you should follow your insurance provider's claims process. This may involve reporting the accident to your insurance provider, providing details of the accident, and cooperating with any investigations.</p> 
  </span> </div> 
<label for="post-8" class="eight-trigger">8. What happens if one of my vehicles breaks down?</label> 
<div class="eight-wrap"> <span class="eight-target"> 
  <p>If one of your vehicles breaks down, you may be able to use your fleet breakdown cover to receive roadside assistance, towing, and repair services. You should contact your insurance provider to determine the specific benefits of your policy</p> 
