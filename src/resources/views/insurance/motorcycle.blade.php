<label for="post-3" class="third-trigger">3. What types of coverage are available for motorcycle insurance?</label> 
<div class="third-wrap"> <span class="third-target"> 
  <p>There are several types of coverage available for motorcycle insurance, including liability coverage, collision coverage, comprehensive coverage, uninsured/underinsured motorist coverage, and medical payments coverage.</p> 
  </span> </div> 
