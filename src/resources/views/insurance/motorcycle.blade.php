<label for="post-2" class="second-trigger">2. What factors affect the cost of motorcycle insurance?</label> 
<div class="second-wrap"> <span class="second-target"> 
  <p>Several factors can affect the cost of motorcycle insurance, including your driving record, the make and model of your motorcycle, your age and gender, the level of coverage you choose, and where you live.</p> 
